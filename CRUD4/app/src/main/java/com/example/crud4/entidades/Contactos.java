package com.example.crud4.entidades;

public class Contactos {
    private int id;
    private String nombre;
    private String cinta;
    private String categoria;
    private String escuela;
    private String compite;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getCinta() {
        return cinta;
    }

    public void setCinta(String cinta) {
        this.cinta = cinta;
    }

    public String getCategoria() {
        return categoria;
    }

    public void setCategoria(String categoria) {
        this.categoria = categoria;
    }

    public String getEscuela() {
        return escuela;
    }

    public void setEscuela(String escuela) {
        this.escuela = escuela;
    }

    public String getCompite() {
        return compite;
    }

    public void setCompite(String compite) {
        this.compite = compite;
    }
}
